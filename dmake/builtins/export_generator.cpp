Target* find_target_in_context(const ExportContext& ctx, const std::string& name) {
    if (!ctx.all_targets) return nullptr;
    auto it = ctx.all_targets->find(name);
    if (it != ctx.all_targets->end()) return it->second.get();
    if (ctx.target_aliases) {
        auto alias_it = ctx.target_aliases->find(name);
        if (alias_it != ctx.target_aliases->end()) {
            it = ctx.all_targets->find(alias_it->second);
            if (it != ctx.all_targets->end()) return it->second.get();
        }
    }
    return nullptr;
}

// Convert string to uppercase
static std::string to_upper(const std::string& str) {
    std::string result = str;
    for (char& c : result) {
        c = static_cast<char>(std::toupper(static_cast<unsigned char>(c)));
    }
    return result;
}

// Generate the IMPORT_PREFIX computation code for install exports
static std::string generate_import_prefix_code(const std::string& destination) {
    std::ostringstream ss;

    // Count directory depth from install prefix to destination
    std::filesystem::path dest_path(destination);
    int depth = 0;
    for (const auto& part : dest_path) {
        if (!part.empty() && part != ".") {
            depth++;
        }
    }

    // First, get the directory containing this file
    ss << "get_filename_component(_IMPORT_PREFIX \"${CMAKE_CURRENT_LIST_FILE}\" PATH)\n";

    // Navigate up to the install prefix
    for (int i = 0; i < depth; ++i) {
        ss << "get_filename_component(_IMPORT_PREFIX \"${_IMPORT_PREFIX}\" PATH)\n";
    }

    // Verify we ended up at a valid path
    ss << "if(_IMPORT_PREFIX STREQUAL \"/\")\n";
    ss << "  set(_IMPORT_PREFIX \"\")\n";
    ss << "endif()\n";

    return ss.str();
}

