// Generate CMake header with version check and policy management
std::string generate_cmake_header() {
    std::ostringstream ss;
    ss << "# Generated by dmake - do not edit\n\n";

    // CMake version check
    ss << "if(CMAKE_VERSION VERSION_LESS \"3.5\")\n";
    ss << "  message(FATAL_ERROR \"CMake >= 3.5 required for this export file\")\n";
    ss << "endif()\n\n";

    // Policy management
    ss << "cmake_policy(PUSH)\n";
    ss << "cmake_policy(VERSION 3.5)\n\n";

    // Import file version
    ss << "set(CMAKE_IMPORT_FILE_VERSION 1)\n\n";

    return ss.str();
}

// Generate expected targets check to protect against multiple inclusion
std::string generate_expected_targets_check(
    const std::vector<Target*>& targets,
    const std::string& namespace_prefix
) {
    std::ostringstream ss;

    // Build list of expected target names
    ss << "# Protect against multiple inclusion\n";
    ss << "set(_cmake_targets_defined \"\")\n";
    ss << "set(_cmake_targets_not_defined \"\")\n";
    ss << "foreach(_cmake_expected_target IN ITEMS";
    for (const auto* t : targets) {
        if (!t || t->get_type() == TargetType::CUSTOM) continue;
        ss << " " << namespace_prefix << t->get_name();
    }
    ss << ")\n";
    ss << "  if(TARGET \"${_cmake_expected_target}\")\n";
    ss << "    list(APPEND _cmake_targets_defined \"${_cmake_expected_target}\")\n";
    ss << "  else()\n";
    ss << "    list(APPEND _cmake_targets_not_defined \"${_cmake_expected_target}\")\n";
    ss << "  endif()\n";
    ss << "endforeach()\n\n";

    // Validation: all defined or all not defined
    ss << "if(_cmake_targets_defined AND _cmake_targets_not_defined)\n";
    ss << "  message(FATAL_ERROR \"Some targets in this export set were already defined\")\n";
    ss << "endif()\n";
    ss << "unset(_cmake_targets_defined)\n";
    ss << "unset(_cmake_targets_not_defined)\n\n";

    return ss.str();
}

// Generate CMake footer with cleanup
std::string generate_cmake_footer() {
    std::ostringstream ss;
    ss << "\n# Cleanup\n";
    ss << "set(CMAKE_IMPORT_FILE_VERSION)\n";
    ss << "cmake_policy(POP)\n";
    return ss.str();
}

// Convert TargetType to the CMake IMPORTED library type string
std::string target_type_to_imported_type(TargetType type) {
    switch (type) {
        case TargetType::EXECUTABLE:
            return "EXECUTABLE";
        case TargetType::SHARED_LIBRARY:
            return "SHARED";
        case TargetType::STATIC_LIBRARY:
            return "STATIC";
        case TargetType::OBJECT_LIBRARY:
            return "OBJECT";
        case TargetType::INTERFACE_LIBRARY:
            return "INTERFACE";
        default:
            return "UNKNOWN";
    }
}

// Escape a string value for CMake syntax (double quotes within quoted strings)
std::string escape_cmake_string(const std::string& str) {
    std::string result;
    result.reserve(str.size());
    for (char c : str) {
        if (c == '"' || c == '\\' || c == '$') {
            result += '\\';
        }
        result += c;
    }
    return result;
}

// Format a list of values as a CMake list (semicolon-separated, properly quoted if needed)
std::string format_cmake_list(const std::vector<std::string>& values) {
    std::string result;
    for (size_t i = 0; i < values.size(); ++i) {
        if (i > 0) result += ";";
        result += values[i];
    }
    return result;
}

// Create a GenexEvaluationContext for export evaluation
GenexEvaluationContext make_export_genex_context(
    const Target* target,
    const ExportContext& export_ctx
) {
    GenexEvaluationContext ctx;
    ctx.build_type = export_ctx.build_type;
    ctx.system_name = export_ctx.system_name;
    ctx.cxx_compiler_id = export_ctx.cxx_compiler_id;
    ctx.c_compiler_id = export_ctx.c_compiler_id;
    ctx.cxx_compiler_version = export_ctx.cxx_compiler_version;
    ctx.c_compiler_version = export_ctx.c_compiler_version;
    ctx.all_targets = export_ctx.all_targets;
    ctx.target_aliases = export_ctx.target_aliases;
    ctx.current_target = target;
    ctx.install_prefix = export_ctx.install_prefix;
    ctx.phase = export_ctx.for_install ? GenexEvaluationContext::Phase::INSTALL
                                       : GenexEvaluationContext::Phase::BUILD;
    return ctx;
}

// Evaluate a property list with generator expressions for export
std::vector<std::string> evaluate_export_property(
    const Target& target,
    const std::string& prop_name,
    const ExportContext& export_ctx
) {
    // Get the INTERFACE property values
    auto values = target.get_property_list(prop_name, TargetPropertyScope::INTERFACE);
    if (values.empty()) {
        return {};
    }

    auto ctx = make_export_genex_context(&target, export_ctx);
    GenexEvaluator evaluator(ctx);

    auto result = evaluator.evaluate_property_list(values);
    if (!result) {
        // On error, return empty - caller can check target later
        return {};
    }

    return *result;
}

